#!/bin/bash

# Остановить предыдущие контейнеры, если они запущены
echo "Остановка предыдущих контейнеров..."
docker-compose down

# Перестроить и запустить контейнеры
echo "Запуск контейнеров..."
docker-compose up -d

# Проверить статус контейнеров
echo "Статус контейнеров:"
docker-compose ps

# Вывести логи запуска
echo "Логи запуска контейнеров:"
docker-compose logs --tail=50 